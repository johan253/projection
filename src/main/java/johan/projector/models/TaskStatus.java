package johan.projector.models;

/**
 * This enum represents the possible Status that every Task can have.
 *
 * @author Johan Hernandez
 * @version 1.0.0
 */
public enum TaskStatus {
    /**
     * Used to mark when a Task is Unfinished
     */
    UNFINISHED,
    /**
     * Used to mark when a Task is In Progress
     */
    IN_PROGRESS,
    /**
     * Used to mark when a Task is Finished
     */
    FINISHED
}
